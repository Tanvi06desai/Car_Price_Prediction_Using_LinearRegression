"""#Issues with the data
1. KMS_DRIVEN, YEAR AND Price have to be integers
2. KMS_DRIVEN AND FUEL_TYPE have null values
3. Name of the vehicle also needs to be corrected
"""

df['year'].unique() #Year has a lot of non year values

#Only keep the numeric values
df = df[df['year'].str.isnumeric()]

df['year'].unique()

df['year']= df['year'].astype(int)

df['Price'].unique() #Remove the 'Ask For Price' and also conver price to integer

df = df[df['Price']!='Ask For Price']

#Replace the "," with "" in and change the type to integers
df['Price']=df['Price'].str.replace(',','').astype(int)

df['kms_driven'].unique()

#Kms_Driven needs to be converted to numerical data type and also the "," need to be replaced with "" and the kms needs to be removed
df['kms_driven']=df['kms_driven'].str.split().str.get(0).str.replace(',','')

df=df[df['kms_driven'].str.isnumeric()]

df['kms_driven']=df['kms_driven'].astype(int)

df = df[~df['fuel_type'].isna()]

df.shape

df.info()

#Name and company name of the cars need to be changed (keep only first three words)
df['name'] = df['name'].str.split().str.slice(start=0,stop=3).str.join(' ')

#Reseting the index numbers for the cleaned data
df = df.reset_index(drop=True)

